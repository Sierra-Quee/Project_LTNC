    private void btthemnhanvienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemnhanvienActionPerformed
        // TODO add your handling code here:
        String manhanvien = "", tennhanvien = "", cccd = "", diachi = "", chucvu = "",
                ngaythangnamsinhnv = "", sodienthoainv = "", quequan = "";
        boolean check = true;
        manhanvien = txtmanhanvien.getText();
        if (txttennhanvien.getText().length() > 0) {
            tennhanvien = txttennhanvien.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên nhân viên");
            check = false;
        }
        if (txtcccd.getText().length() > 0) {
            cccd = txtcccd.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập CCCD");
            check = false;
        }
        if (txtsodienthoainv.getText().length() > 0) {
            sodienthoainv = txtsodienthoainv.getText();
            
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập SÐT");
            check = false;
        }
        if (txtngaythangnamsinhnv.getText().length() > 0) {
            ngaythangnamsinhnv = txtngaythangnamsinhnv.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập ngày sinh");
            check = false;
        }
        if (txtdiachinv.getText().length() > 0) {
            diachi = txtdiachinv.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập địa chỉ");
            check = false;
        }
        if (txtquequan.getText().length() > 0) {
            quequan = txtquequan.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập quê quán");
            check = false;
        }
        chucvu = (String) cbchucvunv.getSelectedItem();

        if(check) {
            Nhanvien nhanvien = new Nhanvien(manhanvien, tennhanvien,
                    ngaythangnamsinhnv, cccd,
                    sodienthoainv, diachi,
                    quequan,chucvu);
            home.themnhanvien(nhanvien);
            JOptionPane.showMessageDialog(rootPane, " Thêm nhân viên thành công!");
        }
        
    }//GEN-LAST:event_btthemnhanvienActionPerformed

