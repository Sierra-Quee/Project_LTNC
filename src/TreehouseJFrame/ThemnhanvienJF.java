    private void txtmanhanvienFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtmanhanvienFocusLost
        // TODO add your handling code here:
        if (this.isVisible()) {
            String manhanvien = txtmanhanvien.getText();
            // Định dạng mã sản phẩm là 2 chữ, 3 sô
            String madinhdang = "^\\w{2}\\d{3}$";
            String dinhdang = String.valueOf(manhanvien.charAt(0)) + String.valueOf(manhanvien.charAt(1));
            if (manhanvien.length() > 0) {
                if (!manhanvien.matches(madinhdang) || !dinhdang.equals("NV")) {
                    JOptionPane.showMessageDialog(rootPane, "Sai định dạng mã nhân viên! ");
                    txtmanhanvien.requestFocus(); // Yeu cau nhap lai không được phép nhập cái khác
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Mã nhân viên không được để trống !");
                txtmanhanvien.requestFocus();
            }
        }
    }//GEN-LAST:event_txtmanhanvienFocusLost

