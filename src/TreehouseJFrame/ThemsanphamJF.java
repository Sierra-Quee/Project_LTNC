    private void txtmasanphamFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtmasanphamFocusLost
        // TODO add your handling code here: 
        if(this.isVisible()){    
            String masanpham = txtmasanpham.getText();
            // Định dạng mã sản phẩm là 2 chữ, 3 số
            String mau = "^\\w{2}\\d{3}$";
            if (masanpham.length() > 0){
                if (! masanpham.matches(mau)){
                    JOptionPane.showMessageDialog(rootPane, "Sai định dạng mã sản phẩm! ");
                    txtmasanpham.requestFocus(); // Yeu cau nhap lai không được phép nhập cái khác
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Mã sản phẩm không được để trống !");
                txtmasanpham.requestFocus();
            }
        }
    }//GEN-LAST:event_txtmasanphamFocusLost

