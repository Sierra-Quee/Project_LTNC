    private void btthemsanphamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemsanphamActionPerformed
        // TODO add your handling code here:
        String masanpham, tensanpham, mota, hinhanh;
        int soluong = 0;
        float dongia = 0;
        
        masanpham = txtmasanpham.getText();
        tensanpham = txttensanpham.getText();
        mota = txtmota.getText();
        hinhanh = txthinhanh.getText();
        boolean dulieusai = true;
        
        try{
            soluong = Integer.parseInt(txtsoluong.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, 
                    "Số lượng phải là số và không chứa các kí tự khác! ");
            dulieusai = false;
        }
        
        try {
            dongia = Float.parseFloat(txtdongia.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,
                    "Đơn giá phải là số và không chứa các kí tự khác!");
            dulieusai = false;
        }
        if (dulieusai){
            Sanpham sanpham = new Sanpham(masanpham,tensanpham,
                                          hinhanh,mota,dongia,soluong);
            home.themsanpham(sanpham);
            JOptionPane.showMessageDialog(rootPane, " Them san pham thanh cong!");
            txtmasanpham.setText("");
            txttensanpham.setText("");
            txtmota.setText("");
            txtdongia.setText("");
            txtsoluong.setText("");
            txthinhanh.setText("");
        }       
    }//GEN-LAST:event_btthemsanphamActionPerformed

