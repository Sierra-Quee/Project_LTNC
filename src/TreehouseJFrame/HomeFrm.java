    private void bttimkiemtheotenspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttimkiemtheotenspActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập tên sản phẩm cần tìm kiếm");
        if(input.length() > 0) {
            sanphamList = San_pham_controller.TimKiemTheoTen(input);
            if(sanphamList.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sản phẩm không tồn tại !");
            } else {
                tbModelsp.setRowCount(0);// Reset noi dung trong bang ve 0
                sanphamList.forEach((indexsp) -> {
                    tbModelsp.addRow(new Object[]{indexsp.getMasanpham(),
                        indexsp.getTensanpham(), indexsp.getHinhanh(), indexsp.getMota(),
                        indexsp.getDongia(), indexsp.getSoluong()});
                });
            }
        }
    }//GEN-LAST:event_bttimkiemtheotenspActionPerformed

