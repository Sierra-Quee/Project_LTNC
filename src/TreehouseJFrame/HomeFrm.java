    private void bttimkiemtheomanvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttimkiemtheomanvActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập mã nhân viên cần tìm kiếm");
        if(input.length()> 0) {
            nhanvienList = Nhan_vien_controller.TimKiemTheoMaNV(input);
            if(nhanvienList.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nhân viên không có trong danh sách !");
            } else {
                tbModelnv.setRowCount(0);// Reset noi dung trong bang ve 0
                nhanvienList.forEach((indexnv) -> {
                    tbModelnv.addRow(new Object[]{indexnv.getManhanvien(),
                        indexnv.getTennhanvien(),indexnv.getNgaythangnamsinh(),
                        indexnv.getSoCCCD(),indexnv.getSodienthoai(),
                        indexnv.getDiachi(), indexnv.getChucvu()});
                });
            }
        }
    }//GEN-LAST:event_bttimkiemtheomanvActionPerformed

    private void btsuanhanvienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsuanhanvienActionPerformed
        // TODO add your handling code here:
        selectedindexnv = tbnhanvien.getSelectedRow(); // Tra ve so nguyen gia tri chi so hang ta chon sưa
        if (nhanvienList.size() == 0) {
            JOptionPane.showMessageDialog(rootPane,
                "Không có nhân viên");
        } else if (selectedindexnv < 0) {
            JOptionPane.showMessageDialog(rootPane,
                "Hãy chọn nhân viên cần thực hiện sửa!");
        } else {
            SuanhanvienJF sua = new SuanhanvienJF(this, rootPaneCheckingEnabled);
            sua.Suadulieunv(nhanvienList.get(selectedindexnv));
            sua.setVisible(true);
            Hienthidulieunv();
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin thành công.");
        }
    }//GEN-LAST:event_btsuanhanvienActionPerformed

